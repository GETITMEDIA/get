  function showCookieConsent() {
    const cookieConsent = localStorage.getItem('cookieConsent');

    if (!cookieConsent && !cookieShown) {
      const popup = document.querySelector('.cookie-consent');
      if (popup) {
        popup.style.display = 'block';
        cookieShown = true;
      }
    }
  }

